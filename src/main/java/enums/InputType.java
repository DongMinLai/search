package enums;

public enum InputType {
    /**
     *  文本
     */
    TEXT,

    /**
     * 列表
     */
    SELECT,

    /**
     * 日期格式
     */
    DATE,

    /**
     * 时间格式
     */
    DATETIME,

    /**
     * 布尔值（开关）
     */
    BOOLEAN,

    /**
     * 区间
     */
    BETWEEN,

    /**
     * 日期区间
     */
    BETWEEN_DATE,

    /**
     * 时间区间
     */
    BETWEEN_DATETIME
}
